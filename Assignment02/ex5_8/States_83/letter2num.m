function num = letter2num(letter)
    switch(letter)
        case 'a', num = 1;
        case 'b', num = 2;
        case 'c', num = 3;
        case 'd', num = 4;
        case 'e', num = 5;
        case 'f', num = 6;
        case 'g', num = 7;
        case 'h', num = 8;
        case 'i', num = 9;
        case 'j', num = 10;
        case 'k', num = 11;
        case 'l', num = 12;
        case 'm', num = 13;
        case 'n', num = 14;
        case 'o', num = 15;
        case 'p', num = 16;
        case 'q', num = 17;
        case 'r', num = 18;
        case 's', num = 19;
        case 't', num = 20;
        case 'u', num = 21;
        case 'v', num = 22;
        case 'w', num = 23;
        case 'x', num = 24;
        case 'y', num = 25;
        case 'z', num = 26;
        otherwise, num = 0;
    end
end